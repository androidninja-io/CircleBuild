package io.androidninja.models;

public class Artifact {

    int node_index;
    String path;
    String pretty_path;
    String url;

    public int getNodeIndex() {
        return node_index;
    }

    public String getPath() {
        return path;
    }

    public String getPrettyPath() {
        return pretty_path;
    }

    public String getUrl() {
        return url;
    }
}
